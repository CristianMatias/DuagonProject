CREATE TABLE PRICES (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    BRAND_ID INT,
    START_DATE DATE,
    END_DATE DATE,
    PRICE_LIST VARCHAR(255),
    PRODUCT_ID VARCHAR(255),
    PRIORITY VARCHAR(255),
    PRICE INT,
    CURR VARCHAR(255)
);



